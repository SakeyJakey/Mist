public abstract class Theme {

	public abstract int getPrimaryColour();
	public abstract int getPrimaryDimColour();
	public abstract int getSecondaryColour();
	public abstract int getSecondaryDimColour();
	public abstract int getBackgroundColour();
	public abstract int getBackgroundDimColour();
	public abstract int getForegroundColour();
	public abstract int getForegroundDimColour();

}
